import datetime

start_time = datetime.datetime(2023, 10, 1, 8, 0, 0)
end_time = datetime.datetime(2023, 10, 4, 17, 0, 0)
duration = end_time - start_time
print("Durasi antara tanggal dan waktu:", duration)
